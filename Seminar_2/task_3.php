<?php
$regions = [
    "Московская область" => ["Москва", "Красногорск", "Звенигород"],
    "Ленинградская область" => ["Санкт-Петербург", "Кронштадт", "Всеволжск"],
    "Ярославская область" => ["Ярославль", "Рыбинск", "Углич"]
];
foreach($regions as $reg => $towns) {
    $line = "";
    foreach($towns as $town) {
        $line = $line . $town . ", ";
    }
    $line = trim($line, ", ");
    echo $reg . ": " . $line . PHP_EOL;
}
